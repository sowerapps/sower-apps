const SwGeoData & SwGeo::GetData(swUI32 id)
{
    if (id >= N_GEO_LOCATIONS)
        return GeolocationData[0];

    return GeolocationData[id];
}

bool IsInRange(int a, int b)
{
    if (a == b)
        return true;
    if (a < b && b - a <= 3)
        return true;
    if (b > a && a - b <= 3)
        return true;

    return false;
}

swUI32 SwGeo::FindByName(const char * name)
{
    swUI32 pos = 0;
    if (!name)
        return NODE_ID_INVALID;

    while (pos < N_GEO_LOCATIONS)
    {
        if (stricmp(name, GetData(pos).name) == 0)
            return pos;

        pos ++;
    }

    return NODE_ID_INVALID;
}

void SwGeo::GetMapLocation(swUI32 id, const SwGeoConversionData & cd, SwPoint & point)
{
    CalculatePosition(cd, GetData(id).coords, point);
}

swUI32 SwGeo::IdentifyLocation(const SwPoint & position, const SwGeoConversionData & cd)
{
    SwPoint point;

    for (swUI32 i = 1; i < N_GEO_LOCATIONS; i ++)
    {
        GetMapLocation(i, cd, point);

        if (IsInRange(position.x, point.x) && IsInRange(position.y, point.y))
            return i;
    }

    return NODE_ID_INVALID;
}

void SwGeo::CalculatePosition(const SwGeoConversionData & cd, const SwGeoCoordinates & coords, SwPoint & point)
{
    swFloat longitudedelta = cd.longitudeRight - cd.longitudeLeft;
    swFloat lattitudebottomradian = cd.latitudeBottom * swpi / 180;
    swFloat latitude = coords.latitude * swpi / 180;
    swFloat worldmapradius = ((cd.mapwidth / longitudedelta) * 360) / (2 * swpi);
    swFloat offsetY = (worldmapradius / 2 * log((1 + sin(lattitudebottomradian)) / (1 - sin(lattitudebottomradian))));

    point.x = (coords.longitude - cd.longitudeLeft) * (cd.mapwidth / longitudedelta);
    point.y = cd.mapheight - ((worldmapradius / 2 * log((1 + sin(latitude)) / (1 - sin(latitude)))) - offsetY);
}

void SwGeo::CalculatePositionPoint(const SwGeoConversionData & cd, SwGeoCoordinates & coords, const SwPoint & point)
{
    swFloat longitudedelta = cd.longitudeRight - cd.longitudeLeft;
    swFloat lattitudebottomradian = cd.latitudeBottom * swpi / 180;
    swFloat worldmapradius = cd.mapwidth / longitudedelta * 360 /(2 * swpi);
    swFloat  offsetY = (worldmapradius / 2 * log((1 + sin(lattitudebottomradian)) / (1 - sin(lattitudebottomradian))));
    swFloat equatorY = cd.mapheight + offsetY;
    swFloat a = (equatorY - point.y) / worldmapradius;

    coords.latitude = 180 / swpi * (2 * atan(exp(a)) - swpi / 2);
    coords.longitude = cd.longitudeLeft + point.x / cd.mapwidth * longitudedelta;
}

bool SwGeo::IsLocationValid(swUI32 location, SwGeoConversionData & cd)
{
    SwPoint mapPosition;

    GetMapLocation(location, cd, mapPosition);

    if (mapPosition.x < 1 || mapPosition.y < 1 || (unsigned) mapPosition.x > cd.mapwidth || (unsigned) mapPosition.y > cd.mapheight)
        return false;

    return true;
}

void SwGeo::FillCtrl(wxChoice * choice)
{
    if (!choice)
        return;

    swUI32 pos = 1;
    SwStringW buffer;
    SwClientData * data;

    while (pos < N_GEO_LOCATIONS)
    {
        data = new SwClientData(GetData(pos).id);
        buffer.Copy(GetData(pos).name);
        choice->Append(buffer.GetArray(), data);

        pos ++;
    }
}

void SwGeo::FillCtrl(wxListCtrl * listCtrl)
{
    if (!listCtrl)
        return;

    SwStringW buffer;
    wxListItem lItem;

    for (swUI32 i = 1; i < N_GEO_LOCATIONS; i++)
    {
        lItem.Clear();
        lItem.SetStateMask(wxLIST_MASK_TEXT|wxLIST_MASK_DATA);
        lItem.SetColumn(0);
        lItem.SetId(listCtrl->GetItemCount());
        buffer.Copy(GetData(i).name);
        lItem.SetText(buffer.GetArray());
        lItem.SetData(GetData(i).id);
        listCtrl->InsertItem(lItem);
    }
}
